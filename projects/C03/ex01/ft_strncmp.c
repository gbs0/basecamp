#include <stdio.h>
#include <stdlib.h>

int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int	i;

	i = 0;
	if (s1[i] == '\0' && s2[i] == '\0')
	{
		return (0);
	}
	else
	{
		while (s1[i] == s2[i] && s1[i] != '\0' && s2[i] != '\0' && i < n)
			i++;
		if (i == n)
		{
			return (0);
		}
		else
		{
			return (s1[i] - s2[i]);
		}
	}
}
