#include <unistd.h>
#include <stdio.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_putstr(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		ft_putchar(str[i]);
		i++;
	}
}

int	main(int argc, char **argv)
{
	int	args;
    
	args = argc;
	while (args > 1)
	{
		ft_putstr(argv[args - 1]);
		ft_putchar('\n');
		args--;
	}
	return (0);
}