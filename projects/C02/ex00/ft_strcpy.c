#include <unistd.h>

char *ft_strcpy(char *dest, char *src)
{
    int     i;
    i = 0;
    
    while(i <= (sizeof(src) + 1))
    {
        dest[i] = src[i];
        i++;
    }
    
    return(dest);
}

